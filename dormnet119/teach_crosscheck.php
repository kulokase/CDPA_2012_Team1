		<li>如果網路不能使用，報修前請做交叉測試，並且在報修時說明交叉測試結果。</li>
		<li>交叉測試共三階段
		<ul>
			<li>第一階段：借用室友的電腦以及網路線，接到自己的網路孔，若可以連線則排除網路孔問題。</li>
			<li>第二階段：借用室友的網路線，再以自己的電腦接到網路孔，若可以連線則排除網路線問題。</li>
			<li>第三階段：使用自己的電腦以及網路線連接室友的網路孔，若還是不能連線，則再次檢查設定之後報修。</li>
		</ul>
		</li>
